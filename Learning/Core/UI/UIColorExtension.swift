import UIKit

extension UIColor {
    static var main: UIColor {
        return UIColor(red: 242.0 / 255.0, green: 242.0 / 255.0, blue: 246.0 / 255.0, alpha: 1)
    }
    static var brightYellow: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 205.0 / 255.0, blue: 0.0 / 255.0, alpha: 1)
    }
}
